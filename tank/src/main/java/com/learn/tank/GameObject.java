package com.learn.tank;

import java.awt.*;

public abstract class GameObject {

    public abstract void paint(Graphics g);

    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
